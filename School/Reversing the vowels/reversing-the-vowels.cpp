class Solution
{
    public:
        string modify (string s)
        {
            string result = "";
            int l = s.size();
            for(int i = 0; i < l; i++)
            {
                if(s[i] == 'a' || s[i] == 'e' || s[i] == 'i' || s[i] == 'o' || s[i] == 'u')
                {
                    result.insert(0, 1, s[i]);
                }
            }
            int count = 0;
            for(int i = 0; i < l; i++)
            {
                if(s[i] == 'a' || s[i] == 'e' || s[i] == 'i' || s[i] == 'o' || s[i] == 'u')
                {
                    s[i] = result[count];
                    count++;
                }
            }
            return s;
            // return result;
        }
};

