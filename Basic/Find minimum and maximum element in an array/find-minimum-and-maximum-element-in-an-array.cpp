class Solution {
public:
    pair<long long, long long> getMinMax(long long a[], int n) {
        long long max_value = INT_MIN, min_value = INT_MAX;
        for(int i = 0; i < n; i++){
            max_value = max(max_value, a[i]);
            min_value = min(min_value, a[i]);
        }
        
        return make_pair(min_value,max_value);
    }
};

